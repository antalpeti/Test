package testpackage1;

public class Casting {
  public static void main(String[] args) {
    double d = 100.2d;
    int i = (int) d;
    int s = (int) d;
    byte b = (byte) d;
    char c = (char) d;
    System.out.println(d);
    System.out.println(i);
    System.out.println(s);
    System.out.println(b);
    System.out.println(c);
  }
}
